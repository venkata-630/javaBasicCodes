package org.example.javaBasicCodes;

public class VariblesAndDataType {
    public static void main(String[] args) {
        int age = 10; // Integer data type
        double salary = 20.5; // Double data type
        char c = 'A'; // Character data type
        boolean trueOrFlase = true; // Boolean data type
        String e = "Hello"; // String data type
        // Printing the variables
        System.out.println("Integer: " + age);
        System.out.println("Double: " + salary);
        System.out.println("Character: " + c);
        System.out.println("Boolean: " + trueOrFlase);
        System.out.println("String: " + e);
    }
}
