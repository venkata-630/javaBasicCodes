package org.example.javaBasicCodes;

public class LoopsForWhile {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("Count: " + i);
        }

        int j = 1;
        while (j <= 5) {
            System.out.println("While Loop: " + j);
            j++;
        }
    }
}
